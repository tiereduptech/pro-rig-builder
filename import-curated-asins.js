async function main() {
  const mod = await import(`file://${process.cwd().replace(/\\/g, '/')}/src/data/parts.js`);
  const parts = [...mod.PARTS];
  let nextId = Math.max(...parts.map(p => p.id || 0)) + 1;

  const existingAsins = new Set(parts.filter(p => p.asin).map(p => p.asin));
  const existingTitles = new Set(parts.map(p =>
    String(p.n).toLowerCase().replace(/[^\w\s]/g, ' ').replace(/\s+/g, ' ').trim()
  ));

  const imported = { SoundCard: 0, OpticalDrive: 0, EthernetCard: 0 };
  const skipped = { SoundCard: 0, OpticalDrive: 0, EthernetCard: 0 };

  for (const entry of CURATED) {
    const normTitle = entry.n.toLowerCase().replace(/[^\w\s]/g, ' ').replace(/\s+/g, ' ').trim();
    if (existingAsins.has(entry.asin) || existingTitles.has(normTitle)) {
      skipped[entry.c]++;
      continue;
    }
    const prod = {
      id: nextId++,
      n: entry.n,
      img: entry.img || '',
      c: entry.c,
      b: entry.b,
      pr: entry.pr,
      msrp: entry.msrp,
      r: entry.r,
      reviews: entry.reviews,
      asin: entry.asin,
      deals: {
        amazon: {
          price: entry.pr,
          url: `https://www.amazon.com/dp/${entry.asin}?tag=${AMAZON_TAG}`,
          inStock: true,
        },
      },
    };
    // Category-specific spec fields
    const specFields = Object.keys(entry).filter(k =>
      !['c', 'b', 'n', 'asin', 'pr', 'msrp', 'r', 'reviews', 'img'].includes(k)
    );
    for (const f of specFields) prod[f] = entry[f];

    parts.push(prod);
    existingAsins.add(entry.asin);
    existingTitles.add(normTitle);
    imported[entry.c]++;
  }

  const source = `// Auto-merged catalog. Edit with care.\nexport const PARTS = ${JSON.stringify(parts, null, 2)};\n\nexport default PARTS;\n`;
  writeFileSync('./src/data/parts.js', source);

  console.log('━━━ IMPORTED ━━━');
  for (const cat of Object.keys(imported)) {
    const total = parts.filter(p => p.c === cat).length;
    console.log(`  ${cat.padEnd(14)} +${imported[cat]} new (skipped ${skipped[cat]})  = ${total} total`);
  }
  console.log('\nWrote parts.js');
}

main().catch(e => { console.error(e); process.exit(1); });
